import Foundation

// Framebuffer for the G13 monochrome LCD (160x43 pixels).
//
// Memory layout:
//   The screen is divided into 6 horizontal bands of 8 pixels tall.
//   Each byte represents a column of 8 pixels within a band.
//   For pixel (x, y): index = (y/8) * 160 + x, bit = y % 8
class G13LCD {

    static let width  = 160
    static let height = 43
    static let rows   = 6
    static let bufferSize = width * rows

    var buffer: [UInt8]

    init() {
        buffer = [UInt8](repeating: 0, count: G13LCD.bufferSize)
    }

    func clear() {
        for i in 0..<G13LCD.bufferSize { buffer[i] = 0 }
    }

    func setPixel(x: Int, y: Int, on: Bool = true) {
        guard x >= 0 && x < G13LCD.width && y >= 0 && y < G13LCD.height else { return }
        let idx = (y / 8) * G13LCD.width + x
        if on { buffer[idx] |= UInt8(1 << (y % 8)) }
        else  { buffer[idx] &= ~UInt8(1 << (y % 8)) }
    }

    func drawRect(x: Int, y: Int, width: Int, height: Int) {
        for px in x..<min(x + width, G13LCD.width) {
            setPixel(x: px, y: y)
            setPixel(x: px, y: y + height - 1)
        }
        for py in y..<min(y + height, G13LCD.height) {
            setPixel(x: x, y: py)
            setPixel(x: x + width - 1, y: py)
        }
    }

    func fillRect(x: Int, y: Int, width: Int, height: Int) {
        for py in y..<min(y + height, G13LCD.height) {
            for px in x..<min(x + width, G13LCD.width) {
                setPixel(x: px, y: py)
            }
        }
    }

    func drawHLine(y: Int) {
        for px in 0..<G13LCD.width { setPixel(x: px, y: y) }
    }

    func drawCrosshair(x: Int, y: Int, size: Int = 3) {
        for d in -size...size { setPixel(x: x + d, y: y); setPixel(x: x, y: y + d) }
    }

    // Draw text using the built-in 5x7 font.
    // Each character is 6 pixels wide (5 glyph + 1 spacing).
    // Max 26 chars per line, 5 lines on screen.
    func drawText(_ text: String, x: Int, y: Int) {
        var cx = x
        for char in text {
            if let glyph = G13LCD.font5x7[char] {
                for col in 0..<5 {
                    for row in 0..<7 {
                        if (glyph[col] >> row) & 1 == 1 {
                            setPixel(x: cx + col, y: y + row)
                        }
                    }
                }
            }
            cx += 6
        }
    }

